#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*ps;
	size_t	size;

	if (!s)
		return (NULL);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	size = ft_strlen(s + start);
	if (size < len)
		len = size;
	ps = (char *)malloc(sizeof(char) * (len + 1));
	if (!ps)
		return (NULL);
	ft_strlcpy(ps, s + start, len + 1);
	return (ps);
}
